//this is another way of printing 'Hello World!' in Java.
// the print() method will not shift the cursor to the next line after printing 'Hello World!' whereas the println() will shift the cursor to the next line.
class Java2
{
    public static void main (String[] args)
    {
        System.out.print ("Hello World!");
    }
}

// To run this:
// $ javac Java.java
// $ java Java2
// You will see "Hello World!"

// Set | Part 1

void main() {
  // Set is a list kind of
  var countries = {'Iran', 'US', 'UK'};

  // Add one
  countries.add('Germany');

  // Remove
  countries.remove('Iran');

  // Get first
  print(countries.first);

  // Get last
  print(countries.last);

  // Get lenght
  print(countries.length);

  // Get data with index
  print(countries.elementAt(2));
}

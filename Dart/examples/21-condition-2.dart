// Conditions | Switch

void main() {
  const stage = 1;

  switch (stage) {
    case 1:
      print("Gold!");
      break;
    case 1:
      print("Silver!");
      break;
    case 1:
      print("Bronze!");
      break;
    default:
      print("Sorry, You are not in first 3 :(");
      break;
  }
}

// Operators | Part 2

void main() {
  // += | x = x + n
  int a = 1;
  a += 1; // a = a + 1

  // -= | x = x - n
  int b = 100;
  b -= 1; // b = b + 1

  // *= | x = x * n
  int c = 1;
  c *= 1; // c = c + 1

  // /= | x = x + n
  double d = 1;
  d /= 2; // d = d + 1

  // ~/= | x = x ~/ n
  double e = 50;
  a ~/= 1; // e = e ~/ 1

  // %= | x = x + n
  int f = 1;
  f %= 1; // f = f % 1
}
